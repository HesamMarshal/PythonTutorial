def calculate_area(width, height):
    area = width * height
    print(F"Area = {area}")


width = int(input("Please enter the width: "))
height = int(input("Please enter the height: "))


calculate_area(width, height)
calculate_area(8, 9)
calculate_area(width=8, height=7)
