# Two Layer
for i in range(1, 11):
    for j in range(1, 11):
        print(f'{i} * {j}= {i*j}')

# Three Layer
for i in range(1, 11):
    for j in range(1, 11):
        for k in range(1, 11):
            print(f'{i} * {j} * {k}= {i*j*k}')
